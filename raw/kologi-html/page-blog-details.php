                <p>Thinking is one activity that is generally associated with our mind. Mind is a part of our body which
                  functions ceaselessly what it means is that even when we are asleep we use our mind . That is why
                  mind can be called a factory which produces thoughts. Thousands of thoughts come in our mind
                  everyday. A new study suggested that an average person has 6,200 thoughts per day ( study was
                  published in the journal Nature Communication). Broadly we can categorize thoughts into two types
                  one is positive and the other is negative. Both are equally powerful but at the same time opposite to
                  each other. There has been many research conducted on how positive and negative thinking affects
                  our lives and all research came up with the result that a positive thinking person lives a more happier
                  life than that of a negative thinking person, along with that a positively driven mind fights with
                  difficulties more easily as compared to that of a mind with negative thoughts. This is the reason we
                  perpetually hear from our family , friends, and relatives that “stay positive” and that is why great soul
                  like “Buddha” emphasized on the power of positive thinking by preaching message like “ the mind is
                  everything, what you think , you become “. Hence, we need to take a moment to reflect upon how we
                  are thinking, as our thinking defines our personality thereby holds a greater say in our success or
                  failure.</p>

                <p>What is positive thinking?
                  Positive thinking is when you see a glass half filled rather than half empty which means that when you
                  approach any problem with a positive outlook. It definitely doesn’t mean ignoring all the problems
                  and focus on happiness only. There are many problems around us but instead of cursing and ignoring
                  problems, if one focuses on searching the solutions then it is positive thinking. Positive thinking is not
                  a smile or short term happiness rather it is a habit which when included in life works like a magic.
                  “Chuck Norris” once said “a lot of times people look at the negative side of what they feel they can’t do
                  , I always look on the positive side of what I can do” which clearly means if you keeps a negative
                  attitude towards things you will find everything problematic and if you have a positive attitude
                  towards things you will be able to seek solution of each problems.
                  Effects of positive thinking
                  There are many scientifically proven effects of positive thinking on our health and life. According to a
                  study “positive thinking study” published in 2016 ,in the Journal of Behaviour Research And Therapy
                  by researchers from King college in London , found that when people, diagonised with anxiety
                  disorder were made to visualize the outcome of their worries positively , they reported a decrement in
                  anxiety. So positive thinking not only keeps us happy but also helps in stress and anxiety
                  management. Study also shows that happiness makes you successful rather than success makes you
                  happy. “when people are happy, they tend to feel confident, optimistic and energetic and other finds
                  them likeable and sociable” said Dr. Lyubomirsky, a researcher from university of California,
                  Riverside. As positive thinking is directly related to our health , it has also been found that positive
                  thinking leads to a longer life . This is evident from from the fact that, hermits , ascetics, saints, nuns,
                  monks, stoics etc. used to live a longer life than an ordinary person as they used to view things around
                  them using positive lenses. Power of positive thinking has also been rendered in Bhagwad Gita which
                  says “we have the power to be our own friend and folly to be our own enemy”. Here the enemy is no
                  other than our negative thinking.</p>

                <p>How to inculcate positive thinking?</p>
                <p>“It take same amount of energy to worry as it does to be positive , use your energy to
                  think positive and positive things will happen” – Joel Osteen.</p>
                <p>Having known the miracles of positive thinking, one genuine question that now
                  arises is how to inculcate positive thinking in our life. Though there are many
                  techniques to stay positive and some people might have their own way of staying
                  positive , few useful and proven tips are :</p>
                <ol>

                  <li>Firstly believe on yourself that you can live your life positively.
                  <li>Try to keep yourself happy everyday. Spare few moments from your busy
                    schedule to have a positive self talk. Don’t compare yourself with other and try to be
                    best version of your own and not other.
                  <li>Distract negative thinking with positive thinking i.e. stop negative thinking from
                    taking control of your mind by thinking something positive or thinking about happy
                    moments of your life.
                  <li>Don’t look at what bad happened in your life rather look at the bright side of your
                    life. Try to remember happy moments more than the unhappy one.
                  <li>Be optimistic about any move that you take , don’t think about the bad outcome of
                    an action rather look at the good side of it.
                  <li>Leave the company of those who make you think negatively and surrounds
                    yourself with positive people.
                  <li>Follow a healthy lifestyle as all of us know that a healthy mind resides in a healthy
                    body.
                  <li>Be thankful and grateful to what you have today. And live in present rather
                    than thinking about tomorrow and don’t forget that tomorrow never comes. So
                    live your everyday as if it is your last day. Ask yourself “what would I do if it
                    were to be the last day of my life ?”
                  <li>Stop taking things personally. It is often seen that a person having negative
                    thinking has a bad habit of taking things personally.
                  <li>Try not to worry and always choose a face with smile than a sadistic face.
                    10. Read inspiring stories, quotes and books.
                    So from now instead of saying “I have never done this before” say “ it’s an
                    opportunity to learn something new” instead of saying “ it’s too complicated” say “ I
                    will tackle it with a different angle” instead of saying “ I am sure it will not work” say “
                    I can try to make it work” .
                </ol>



                <h5>Conclusion</h5>
                <p> Positive thinking leads to positive outcomes and negative thinking leads to negative
                  outcomes. Positive attitude is important to live a longer and happy life. We don’t
                  realize but sometimes even a little thing can solve our bigger problems one such little
                  thing is positive thinking which we often ignores but once we use it in our lives it can
                  perform miraculously. If we possess a little thing called positive thinking we can cope
                  up with daily affairs of life. It has the capability to help us grow and get success in our
                  life . It has capability of self-healing. That’s why a little thing called “positive
                  thinking” deserve to be inculcated in our live.</p>
